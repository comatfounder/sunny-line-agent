def generate_questions(brand_name: str, industry: str) -> List[Dict[str, Any]]:
    """
    依品牌名稱與產業，生成客製化問題清單。

    Args:
        brand_name: 品牌名稱（替換問題中的 {品牌名稱}）
        industry:   產業類型（見 SUPPORTED_INDUSTRIES）

    Returns:
        問題清單，每題格式：
        {
            "id":          str,   # 題號（"1", "2", ...）
            "module":      str,   # 模組代號（"M1", "M2", ...）
            "module_name": str,   # 模組顯示名稱
            "question":    str,   # 已替換品牌名稱的問題文字
            "hint":        str,   # 業主看到的提示說明
            "required":    bool,  # 是否必答
            "type":        str,   # text / list / choice / media / sample
            "special":     str|None,  # 特殊邏輯標記
        }

    Raises:
        ValueError: 不支援的產業類型
    """
    bank = _QUESTION_BANKS.get(industry)
    if not bank:
        raise ValueError(
            f"不支援的產業類型：{industry}。"
            f"目前支援：{SUPPORTED_INDUSTRIES}"
        )

    questions = []
    q_num = 1

    for module in bank["modules"]:
        for q in module["questions"]:
            questions.append({
                "id":          str(q_num),
                "module":      module["id"],
                "module_name": module["name"],
                "question":    q["q"].replace("{品牌名稱}", brand_name),
                "hint":        q.get("hint", ""),
                "required":    q.get("required", True),
                "type":        q.get("type", "text"),
                "special":     q.get("special", None),
            })
            q_num += 1

    return questions


def get_service_followup_questions(
    service_name: str, brand_name: str, start_id: int
) -> List[Dict[str, Any]]:
    """
    M2 服務品項追問：對單一服務品項展開 5 題。

    Args:
        service_name: 服務品項名稱（例如「孕婦寫真」）
        brand_name:   品牌名稱
        start_id:     起始題號

    Returns:
        5 題的問題清單
    """
    questions = []
    for i, tmpl in enumerate(SERVICE_ITEM_FOLLOWUP):
        questions.append({
            "id":          str(start_id + i),
            "module":      "M2",
            "module_name": f"服務品項詳細 — {service_name}",
            "question":    tmpl["q"].replace("{service}", service_name).replace("{品牌名稱}", brand_name),
            "hint":        tmpl["hint"],
            "required":    True,
            "type":        "text",
            "special":     None,
        })
    return questions


